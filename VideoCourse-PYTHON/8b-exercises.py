import math
angle = float(input('Type a angle: '))
sin = math.sin(math.radians(angle))
print('The sin of angle {} is {:.2f}'.format(angle, sin))
cos = math.cos(math.radians(angle))
print('The cos of angle {} is {:.2f}'.format(angle, cos))
tan = math.tan(math.radians(angle))
print('The tan of angle {} is {:.2f}'.format(angle, tan))
